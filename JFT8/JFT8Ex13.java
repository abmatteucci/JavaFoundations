package JFT8;

/*
 * Write a Java program, which prints out the numbers from 1-5 using a while
loop.
Save the program as JFT8Ex13.java
 */

public class JFT8Ex13 {

    public static void main(String[] args) {
        
        int i = 1;

        while (i <= 5){
            System.out.println(i);
            i++;
        }

    }
    
}
