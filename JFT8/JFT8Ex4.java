package JFT8;

/*Create a for loop that executes only once. Set the value of the counter to 0. The loop should
run one time.
Save the program as JFT8Ex4.java */

public class JFT8Ex4 {
    public static void main(String[] args) {
        
        int i = 0;
        do
        {
            System.out.println(i);
            break;
        } while (i < 10);
    }
    
}
